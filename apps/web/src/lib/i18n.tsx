const dict: Record<Lang, Copy> = { en, tr };

const LangContext = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: Copy }>({ lang: 'en', setLang: () => {}, t: en });

export function AppLangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');
  useEffect(() => {
    const saved = localStorage.getItem('clickthru-lang');
    if (saved === 'tr' || saved === 'en') setLangState(saved);
  }, []);
  function setLang(l: Lang) {
    setLangState(l);
    try {
      localStorage.setItem('clickthru-lang', l);
    } catch {
      // yoksay
    }
  }
  return <LangContext.Provider value={{ lang, setLang, t: dict[lang] }}>{children}</LangContext.Provider>;
}

export function useT() {
  return useContext(LangContext);
}
